package com.coderhouse.calculadora;

public class Calculadora {

	public void sumar(int numeroA, int numeroB) {
		System.out.println("Numero 1: " + numeroA);
		System.out.println("Numero 2: " + numeroB);
		System.out.println("Resultado: " + (numeroA + numeroB));
	}

	public void sumar(float numeroA, float numeroB) {
		System.out.println("Numero 1: " + numeroA);
		System.out.println("Numero 2: " + numeroB);
		System.out.println("Resultado: " + (numeroA + numeroB));
	}

	public void sumar(double numeroA, double numeroB) {
		System.out.println("Numero 1: " + numeroA);
		System.out.println("Numero 2: " + numeroB);
		System.out.println("Resultado: " + (numeroA + numeroB));
	}
	
}
